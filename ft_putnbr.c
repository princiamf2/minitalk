#include "minitalk.h"

void	ft_putnbr(int nb)
{
	long	nbr;

	nbr = nb;
	if (nbr < 10)
		ft_putchar(nbr + '0');
	else
	{
		ft_putnbr(nbr / 10);
		ft_putnbr(nbr % 10);
	}
}
